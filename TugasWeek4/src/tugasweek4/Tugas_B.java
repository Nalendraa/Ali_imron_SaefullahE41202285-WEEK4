
package tugasweek4;


public class Tugas_B {

    public static void main(String[] args) {

        System.out.println("            Do_While");
        System.out.println("Bilangan kelipatan 2 (1 - 100)");
        System.out.println("=========================");
        int n = 1;
        do {
            System.out.print(n + " ");
            n = n * 2;
        }while (n <= 100);
    }
    
}
